import re

def extract_quoted_phrases(text: str) -> list:
    """
    Извлекает все слова или фразы из текста, заключённые в одинарные (')
    или двойные (") кавычки. Возвращает список найденных фраз без самих кавычек.
    """
    # Регулярное выражение ищет текст внутри одинарных или двойных кавычек.
    # Группа (.*?) захватывает содержимое нежадным способом.
    pattern = r'["\'](.*?)["\']'
    return re.findall(pattern, text)

# Пример входных данных
text = 'Он сказал: "Привет!" и добавил: \'Как дела?\'.'

# Вызов функции и вывод результата
result = extract_quoted_phrases(text)
print("Найденные фразы в кавычках:", result)
